# -------------------------------------------------------------------
async def get_ai_response(messages_history: list) -> str:
    # Временная заглушка – возвращает фиксированный ответ
    # return "Я временно работаю в тестовом режиме. Скоро я смогу полноценно отвечать, а пока расскажите, что вас беспокоит, а я послушаю."

    """Получает ответ от DeepSeek на основе истории сообщений."""
    full_messages = [{"role": "system", "content": SYSTEM_PROMPT}] + messages_history
    try:
        response = client.chat.completions.create(
            model="deepseek-chat",
            messages=full_messages,
            temperature=0.7,
            max_tokens=1000
        )
        return response.choices[0].message.content
    except Exception as e:
        print(f"DeepSeek API error: {e}")
        # Временное fallback-сообщение, чтобы бот не падал
        return "Извините, сейчас возникла техническая проблема. Попробуйте позже или обратитесь к специалисту."