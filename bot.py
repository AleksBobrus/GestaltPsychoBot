# bot.py – первая версия бота-психолога с новым меню.
# Этот файл запускает бота, содержит главную клавиатуру и заглушки для нереализованных кнопок.
# Все обработчики для кнопки "Поговорить" вынесены в отдельный модуль handlers/dialog_handlers.py.

# -------------------------------------------------------------------
# ИМПОРТЫ
# -------------------------------------------------------------------

# asyncio – встроенная библиотека для асинхронного программирования.
# Используется для запуска бота (asyncio.run(main())).
import asyncio

# os – встроенная библиотека для работы с операционной системой.
# Нужна для чтения переменных окружения (токен бота из .env).
import os

# load_dotenv – функция из библиотеки python-dotenv.
# Загружает переменные из файла .env в окружение процесса.
# Используется, чтобы не хранить токен в коде.
from dotenv import load_dotenv

# Bot, Dispatcher – основные классы из aiogram (главная библиотека для ботов).
# Bot – объект для отправки запросов к Telegram API.
# Dispatcher – обрабатывает входящие обновления (сообщения, команды).
from aiogram import Bot, Dispatcher

# types – содержит классы для работы с объектами Telegram (сообщения, кнопки и т.д.).
from aiogram import types

# F – фильтр из aiogram, позволяет удобно проверять текст сообщения (например, F.text == "кнопка").
from aiogram import F

# Command – фильтр для команд (например, Command("start")).
from aiogram.filters import Command

from aiogram.fsm.context import FSMContext          # <--- добавили

# Импортируем клавиатуру из отдельного файла
from keyboards import main_menu_kb

# Импортируем регистратор из модуля диалога (чтобы подключить обработчики "Поговорить")
from handlers.dialog_handlers import register_dialog_handlers


# -------------------------------------------------------------------
# ЗАГРУЗКА ПЕРЕМЕННЫХ ОКРУЖЕНИЯ
# -------------------------------------------------------------------
load_dotenv()                     # читаем .env
BOT_TOKEN = os.getenv("BOT_TOKEN")   # получаем токен бота из переменной окружения

if not BOT_TOKEN:
    raise ValueError("❌ BOT_TOKEN не найден в файле .env! Проверьте настройки.")

# -------------------------------------------------------------------
# ИНИЦИАЛИЗАЦИЯ БОТА И ДИСПЕТЧЕРА
# -------------------------------------------------------------------
bot = Bot(token=BOT_TOKEN)          # создаём объект бота
dp = Dispatcher()                   # создаём диспетчер (принимает и маршрутизирует сообщения)


# -------------------------------------------------------------------
# ОБРАБОТЧИКИ КОМАНД
# -------------------------------------------------------------------
@dp.message(Command("start"))
async def cmd_start(message: types.Message, state: FSMContext):
    """
    Вызывается при отправке пользователем команды /start.
        - Очищает любое активное состояние FSM (на случай, если бот был в диалоге).
        - Отправляет приветствие и показывает главное меню.
    """
    await state.clear()  # сброс состояния (выход из возможного диалога или теста)

    # Получаем имя пользователя
    user_name = message.from_user.first_name or "друг"

    await message.answer(
        f"👋 Привет, {user_name}!\n\n"
        f"Я — ваш **психологический помощник**, созданный для поддержки и понимания.\n\n"
        f"⚠️ **Важно:**\n"
        f"• Я не заменяю профессионального психолога\n"
        f"• При серьёзных проблемах обратитесь к специалисту\n"
        f"• В кризисной ситуации звоните: **8-800-2000-122** (бесплатно, 24/7)\n\n"
        f"Чем могу помочь сегодня?",
        parse_mode="Markdown",
        reply_markup=main_menu_kb
    )

@dp.message(Command("help"))
async def cmd_help(message: types.Message, state: FSMContext):
    """Показывает справку по использованию бота."""
    await state.clear()
    await message.answer(
        "ℹ️ **Справка по боту**\n\n"
        "**Доступные команды:**\n"
        "• /start — Главное меню\n"
        "• /help — Эта справка\n\n"
        "**Функции бота:**\n"
        "• 💬 **Поговорить** — Диалог с ИИ-психологом\n"
        "  - Лимит: 20 сообщений в день\n"
        "  - История сохраняется автоматически\n"
        "• 🌱 **Заземлиться** — (в разработке)\n"
        "• 📋 **Пройти тест** — (в разработке)\n"
        "• 👤 **Личный кабинет** — (в разработке)\n"
        "• ℹ️ **О боте** — Информация о функциях\n\n"
        "**Важно:** Бот не заменяет профессионального психолога!",
        parse_mode="Markdown",
        reply_markup=main_menu_kb
    )

# -------------------------------------------------------------------
# ЗАГЛУШКИ ДЛЯ ВСЕХ КНОПОК (временные ответы)
# -------------------------------------------------------------------
# Каждая такая функция очищает состояние (если пользователь переключился с другой кнопки)
# и выдаёт сообщение о том, что функция в разработке.

@dp.message(F.text == "🌱 Заземлиться")
async def placeholder_grounding(message: types.Message, state: FSMContext):
    """Заглушка для кнопки 'Заземлиться'."""
    await state.clear()
    await message.answer("🛠 Техники заземления появятся позже.", reply_markup=main_menu_kb)

@dp.message(F.text == "📋 Пройти тест")
async def placeholder_test(message: types.Message, state: FSMContext):
    """Заглушка для кнопки 'Пройти тест'."""
    await state.clear()
    await message.answer("🛠 Тест Бека будет доступен в следующей версии.", reply_markup=main_menu_kb)

@dp.message(F.text == "👤 Личный кабинет")
async def placeholder_profile(message: types.Message, state: FSMContext):
    """Заглушка для кнопки 'Личный кабинет'."""
    await state.clear()
    await message.answer("🛠 Личный кабинет в разработке.", reply_markup=main_menu_kb)

@dp.message(F.text == "ℹ️ О боте")
async def about_bot(message: types.Message, state: FSMContext):
    """Информация о боте (не заглушка, а реальное описание)."""
    await state.clear()
    await message.answer(
        "🤖 **Психологический помощник**\n\n"
        "**Доступно сейчас:**\n"
        "• 💬 Беседа с ИИ-психологом (DeepSeek)\n"
        "• 📊 Лимит: 20 бесплатных сообщений в день\n\n"
        "**В разработке:**\n"
        "• 📋 Тест Бека на депрессию\n"
        "• 🌱 Техники заземления и снятия тревоги\n"
        "• 👤 Личный кабинет с историей\n\n"
        "Следите за обновлениями!",
        parse_mode="Markdown",
        reply_markup=main_menu_kb
    )

# -------------------------------------------------------------------
# ПОДКЛЮЧЕНИЕ ОБРАБОТЧИКОВ ИЗ ДРУГИХ МОДУЛЕЙ
# -------------------------------------------------------------------
# Регистрируем все хендлеры, определённые в файле handlers/dialog_handlers.py
# (кнопка "Поговорить", режим диалога, кнопка выхода и временная заглушка).
register_dialog_handlers(dp)

# -------------------------------------------------------------------
# ЗАПУСК БОТА
# -------------------------------------------------------------------
async def main():
    """Точка входа: запуск поллинга (бот опрашивает Telegram на наличие новых сообщений)."""
    print("✅ Бот запущен.")
    print("💬 Кнопка «Поговорить» – работает с DeepSeek, лимит 20 сообщений/день.")
    print("🌱 Кнопка «Заземлиться» – заглушка.")
    print("📋 Кнопка «Пройти тест» – заглушка.")
    print("👤 Кнопка «Личный кабинет» – заглушка.")
    print("ℹ️ Кнопка «О боте» – информация.")
    await dp.start_polling(bot)   # запускаем бесконечный цикл приёма сообщений

if __name__ == "__main__":
    asyncio.run(main())           # запускаем асинхронную функцию main()
