def interpret_score(score: int) -> str:
    """Возвращает интерпретацию на основе суммы баллов."""
    if score <= 9:
        return "Отсутствие или минимальная депрессия (норма)"
    elif score <= 16:
        return "Лёгкая депрессия (субдепрессия)"
    elif score <= 24:
        return "Умеренная депрессия"
    elif score <= 30:
        return "Выраженная депрессия (средней тяжести)"
    else:
        return "Тяжёлая депрессия"