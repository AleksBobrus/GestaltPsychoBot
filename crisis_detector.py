# crisis_detector.py
# Модуль для детекции кризисных ситуаций в сообщениях пользователя

import re
from typing import Tuple

# -------------------------------------------------------------------
# КЛЮЧЕВЫЕ СЛОВА ДЛЯ ДЕТЕКЦИИ
# -------------------------------------------------------------------
CRISIS_KEYWORDS = [
    # Суицидальные мысли
    r"\b(суицид|самоубийств|покончить с собой|не хочу жить|хочу умереть)\b",
    r"\b(убить себя|свести счёты|свести счеты|уйти из жизни)\b",
    r"\b(нет смысла жить|жить не хочу|жизнь не имеет смысла)\b",

    # Самоповреждение
    r"\b(порезать себя|порезаться|нанести себе|причинить себе боль)\b",
    r"\b(селфхарм|self.?harm|самоповрежден)\b",

    # Критическое состояние
    r"\b(не могу больше|сил больше нет|невыносимо больно)\b",
    r"\b(хочу исчезнуть|хочу перестать существовать)\b",
]

# Компилируем регулярки для быстрой проверки
CRISIS_PATTERNS = [re.compile(pattern, re.IGNORECASE) for pattern in CRISIS_KEYWORDS]


# -------------------------------------------------------------------
# ФУНКЦИЯ ДЕТЕКЦИИ
# -------------------------------------------------------------------
def detect_crisis(text: str) -> Tuple[bool, str]:
    """
    Проверяет текст на наличие кризисных маркеров.

    Возвращает:
        (is_crisis: bool, matched_pattern: str)
        - is_crisis: True если обнаружен кризис
        - matched_pattern: найденная фраза (для логирования)
    """
    text_lower = text.lower()

    for pattern in CRISIS_PATTERNS:
        match = pattern.search(text_lower)
        if match:
            return True, match.group(0)

    return False, ""


# -------------------------------------------------------------------
# ТЕКСТ ОТВЕТА ПРИ КРИЗИСЕ
# -------------------------------------------------------------------
CRISIS_RESPONSE = """🆘 **Я заметил, что вам сейчас очень тяжело.**
Пожалуйста, не оставайтесь одни. Обратитесь за профессиональной помощью к психологу или психотерапевту.
Вы не одиноки. Помощь доступна."""


def get_crisis_response() -> str:
    """Возвращает текст ответа при обнаружении кризиса."""
    return CRISIS_RESPONSE
