# crisis_detector.py

CRISIS_KEYWORDS = [
    "суицид", "самоубийство", "покончить с собой", "убить себя", "не хочу жить",
    "нет смысла жить", "выйти из окна", "повешусь", "застрелюсь", "отравлюсь",
    "хочу умереть", "умру", "бесполезно жить", "всё кончено", "не вижу выхода",
    "жить не хочется", "жизнь не имеет смысла"
]

def is_crisis_message(text: str) -> bool:
    text_lower = text.lower()
    for keyword in CRISIS_KEYWORDS:
        if keyword in text_lower:
            return True
    return False

